package com.infectedbytes.carbon.controllers;

/**
 * Mapping of all supported buttons. A controller may have different names for each button.
 * 
 * @author Henrik
 *
 */
public enum CarbonKey {
	/**
	 * A-button on XBox like controllers. Cross-button on Playstation like controllers.
	 */
	A,
	/**
	 * B-button on XBox like controllers. Circle-button on Playstation like controllers.
	 */
	B,
	/**
	 * X-button on XBox like controllers. Square-button on Playstation like controllers.
	 */
	X,
	/**
	 * Y-button on XBox like controllers. Triangle-button on Playstation like controllers.
	 */
	Y,
	/**
	 * Left shoulder button 1.
	 */
	L1,
	/**
	 * Right shoulder button 1.
	 */
	R1,
	/**
	 * Left shoulder button 2, often the same as the left trigger.
	 */
	L2,
	/**
	 * Right shoulder button 2, often the same as the right trigger.
	 */
	R2,
	/**
	 * Left analog stick pressed down.
	 */
	L3,
	/**
	 * Right analog stick pressed down.
	 */
	R3,
	/**
	 * Start or Menu-button.
	 */
	START,
	/**
	 * Back or Select-button.
	 */
	BACK,
}
